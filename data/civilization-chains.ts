/* ─────────── 从活动中自动匹配文明传承链 ─────────── */

export interface ChainMatchResult {
  chain: CivilizationChain;
  matchedActivities: string[];
  score: number;
}

/**
 * 根据活动列表自动匹配文明传承链
 * 返回按匹配度排序的链列表（去重，仅含得分 > 0 的条目）
 */
export function matchChainsFromActivities(
  activities: { title: string; description: string }[],
): ChainMatchResult[] {
  const matchMap = new Map<string, ChainMatchResult>();

  activities.forEach((act) => {
    const text = `${act.title} ${act.description}`;

    CIVILIZATION_CHAINS.forEach((chain) => {
      let score = 0;
      chain.keywords.forEach((kw) => {
        if (text.includes(kw)) score += 1;
      });

      if (score > 0) {
        const existing = matchMap.get(chain.id);
        if (existing) {
          existing.score += score;
          if (!existing.matchedActivities.includes(act.title)) {
            existing.matchedActivities.push(act.title);
          }
        } else {
          matchMap.set(chain.id, {
            chain,
            matchedActivities: [act.title],
            score,
          });
        }
      }
    });
  });

  return Array.from(matchMap.values()).sort((a, b) => b.score - a.score);
}
